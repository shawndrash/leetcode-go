func integerBreak(n int) int {
	if n <= 3 {
		return n - 1
	}

	dp := make([]int, n+1)
	dp[2] = 1
	for i := 3; i <= n; i++ {
		for j := 1; j < i-1; j++ {
			dp[i] = max(dp[i], max((i-j)*j, dp[i-j]*j))
		}
	}

	return dp[n]
}
